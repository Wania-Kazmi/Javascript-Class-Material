//while loop:
//the while loop, loops through a block of code as long as specified condition is true:

//syntax -- while(condition){//code block to be executed}

let i = 5

while(i<10){
    console.log("I "+i);
    i++;
}

//doWhileLoop:
//syntax -- do{//code block to be excuted} while(condition)

do{
    console.log("I "+i);
    i++;
}while(i < 10);
